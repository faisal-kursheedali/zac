const mg = require("mongoose");

const userModel = mg.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const User = mg.model("User", userModel);

module.exports = User;
